namespace FootballManager;

/// <summary>   Enumeration representing different positions in football. </summary>
public enum Position
{
    /// <summary>   Represents the goalkeeper position. </summary>
    Goalkeeper,
    /// <summary>   Represents the defender position. </summary>
    Defender,
    /// <summary>   Represents the midfielder position. </summary>
    Midfielder,
    /// <summary>   Represents the forward position. </summary>
    Forward
}