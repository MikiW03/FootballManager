namespace FootballManager;

/// <summary> Represents a football coach. </summary>
public class Coach()
{
    /// <summary>   Gets or sets the name of the coach. </summary>
    ///
    /// <value> The name of the coach. </value>
    public string Name { get; set; }
    /// <summary>   Gets or sets the nationality of the coach. </summary>
    ///
    /// <value> The nationality of the coach. </value>
    public string Nationality { get; set; }
    /// <summary>   Gets or sets the age of the coach. </summary>
    ///
    /// <value> The age of the coach. </value>
    public int Age { get; set; }
}